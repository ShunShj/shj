CREATE TABLE `ota_subscribe` (
  `ota_subscribe_id` int(10) unsigned NOT NULL AUTO_INCREMENT COMMENT '主键id',
  `uuid` varchar(46) DEFAULT NULL,
  `subscribe_email` varchar(255) DEFAULT NULL COMMENT '邮箱',
  `website_uuid` varchar(46) DEFAULT NULL COMMENT '网站uuid',
  `member_uuid` varchar(46) DEFAULT NULL COMMENT '提交人的uuid',
  `create_time` int(11) DEFAULT NULL COMMENT '订约时间',
  PRIMARY KEY (`ota_subscribe_id`)
) ENGINE=InnoDB AUTO_INCREMENT=4 DEFAULT CHARSET=utf8;
